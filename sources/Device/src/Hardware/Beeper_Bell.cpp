#include "defines.h"
#include "Beeper.h"

static bool bellIsEnabled = false;


/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Beeper::Bell::On()
{
    if (bellIsEnabled)
    {
        return;
    }

    Beeper::WaitForCompletion();

    bellIsEnabled = true;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void Beeper::Bell::Off()
{
    bellIsEnabled = false;
}
