#include "defines.h"
#include "Beeper.h"
#include "Hardware/HAL/HAL.h"


static bool bellIsEnabled = false;


/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Beeper::Bell::On()
{
    if (IsAcitve())
    {
        return;
    }

    Beeper::WaitForCompletion();

    HAL::DAC1_::BellOn();

    bellIsEnabled = true;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void Beeper::Bell::Off()
{
    HAL::DAC1_::BellOff();

    bellIsEnabled = false;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
bool Beeper::Bell::IsAcitve()
{
    return bellIsEnabled;
}
