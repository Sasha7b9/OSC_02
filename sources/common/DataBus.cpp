#include "defines.h"
#include "DataBus.h"


/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
namespace DataBus
{
    static Mode::E mode = Mode::FPGA;
}


/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void DataBus::SetModeTransmit()
{
    mode = Mode::DeviceTransmit;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void DataBus::SetModeReceive()
{
    mode = Mode::DeviceReceive;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void DataBus::SetModeFPGA()
{
    mode = Mode::FPGA;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
DataBus::Mode::E DataBus::GetMode()
{
    return mode;
}
