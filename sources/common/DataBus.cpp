#include "defines.h"
#include "DataBus.h"


/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
namespace DataBus
{
    static Mode mode(Mode::FPGA);
}


/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void DataBus::SetModeTransmit()
{
    mode.value = Mode::DeviceTransmit;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void DataBus::SetModeReceive()
{
    mode.value = Mode::DeviceReceive;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void DataBus::SetModeFPGA()
{
    mode.value = Mode::FPGA;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
DataBus::Mode DataBus::GetMode()
{
    return mode;
}
